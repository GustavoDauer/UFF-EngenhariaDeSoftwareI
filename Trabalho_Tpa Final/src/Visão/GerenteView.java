    private void atualizarProdutoButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_atualizarProdutoButtonMouseClicked
        String codigo = campoEstoqueCodigoProduto.getText();
        String nome = campoNomeProduto.getText();
        String descricao = campoDescricao.getText();
        String preco = campoPreco.getText();
        String quantidade = campoQtdEstoque.getText();
        if(nome.isEmpty()){
            JOptionPane.showMessageDialog(this, "Por favor insira o nome do produto", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(descricao.isEmpty()){
            JOptionPane.showMessageDialog(this, "Por favor insira a descricao do produto", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(preco.isEmpty()){
            JOptionPane.showMessageDialog(this, "Por favor insira o preço do produto", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(quantidade.isEmpty()){
            JOptionPane.showMessageDialog(this, "Por favor insira a quantidade do produto", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        ControleProduto.atualizaProduto(codigo, nome, descricao, preco, quantidade);
        // chama a função de atualização;
        campoNomeProduto.setText("");
        campoDescricao.setText("");
        campoPreco.setText("");
        campoQtdEstoque.setText("");
        alterarProdutoPanel.setVisible(false);
        campoEstoqueCodigoProduto.setText("");
    }//GEN-LAST:event_atualizarProdutoButtonMouseClicked

    private void alterarProdutoButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_alterarProdutoButtonMouseClicked
        String codigo = campoEstoqueCodigoProduto.getText();
        if(codigo.isEmpty()){
            JOptionPane.showMessageDialog(this, "Por favor insira um código", "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(ControleProduto.pertence(codigo)){
            IncluirButton.setVisible(false);
            alterarProdutoButton.setVisible(true);
            alterarProdutoPanel.setVisible(true);
            campoNomeProduto.setText(ControleProduto.getNomeProduto(codigo));
            campoDescricao.setText(ControleProduto.getFornecedor(codigo));
            campoPreco.setText(ControleProduto.getPreco(codigo));
            campoQtdEstoque.setText(ControleProduto.getQuantidade(codigo));
        }
        else{
            JOptionPane.showMessageDialog(this, "Código de produto não encontrado", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_alterarProdutoButtonMouseClicked

