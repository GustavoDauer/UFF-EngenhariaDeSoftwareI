    private void IncluirButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IncluirButtonActionPerformed
        String codigoDeProduto = campoEstoqueCodigoProduto.getText();
        String nome = campoNomeProduto.getText();
        String fornecedor = campoDescricao.getText();
         double valor;
         
        if(codigoDeProduto.isEmpty()){
            JOptionPane.showMessageDialog(this, "Por favor insira o codigo do produto", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        else if(nome.isEmpty()){
            JOptionPane.showMessageDialog(this, "Por favor insira o nome do produto", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        else if(fornecedor.isEmpty()){
            JOptionPane.showMessageDialog(this, "Por favor insira o nome do fornecedor", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        else if(campoPreco.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Por favor insira o preço do produto", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        else{
            valor = Double.parseDouble(campoPreco.getText());
        
            if(campoQtdEstoque.getText().isEmpty())
                JOptionPane.showMessageDialog(this, "Por favor insira a quantidade em estoque", "Erro", JOptionPane.ERROR_MESSAGE);
            else{
                int quantidadeEstoque = Integer.parseInt(campoQtdEstoque.getText());
                if(!ControleProduto.insereProduto(codigoDeProduto, nome, fornecedor, valor, quantidadeEstoque))
                    JOptionPane.showMessageDialog(this, "Código de produto já cadastrado", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
        campoEstoqueCodigoProduto.setText("");
        campoNomeProduto.setText("");
        campoDescricao.setText("");
        campoQtdEstoque.setText("");
        campoPreco.setText("");
        alterarProdutoPanel.setVisible(false);
    }//GEN-LAST:event_IncluirButtonActionPerformed

